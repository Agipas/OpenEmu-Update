#include "sms.h"
#include "port.h"
/* ----------------------------------------------------------------------------------- */
struct PORT ports[2];

/* ----------------------------------------------------------------------------------- */
void port_init();
void port_read();

/* ----------------------------------------------------------------------------------- */
void port_init(){
	PORT_CTRL = 0xFF;
}
/* ----------------------------------------------------------------------------------- */
void port_read(){
	ports[0].button = ~PORT_A;
	ports[1].button = (PORT_A >> 6) + (PORT_B << 2);
	ports[1].button = ~ports[1].button;
}
